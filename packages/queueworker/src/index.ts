export default {
	async queue(batch: MessageBatch<any>, env: Env): Promise<void> {
		for (const message of batch.messages) {
			try {
				const body = message.body as { value: string };
				await env.KV.put(body.value, `id:${message.id}:timestamp:${message.timestamp}`);
				message.ack();
			} catch (e) {
				message.retry();
			}
		}
	},
};
